# 사칙연산

print(2 + 3)
print(2 - 3)
print(2 * 3)
print(2 / 3.0)
print(2.0 / 3)
print(2.0 / 3.0)

# // (몫), ** (지수승), % (나머지)
print(6 // 2)
print(2 ** 3)
print(2 % 3)

# divmod() 함수
print(divmod(2, 3))

# 연산자 우선순위
print( 2 + 3 * 4 )
print( -2 + 3 * 4 )
print( (-2 + 3) * 4 )

# 결합순서 주의 (**는 뒤 먼저)
print(2**3**4)
print((2**3)**4)
print(2**(3**4))

